package com.github.lemerch.columbus.fieldmapper.schema.beans;

public class Bart {
    private Integer id;
    private String shirts;
    private String burger;
    private String spiderpig;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getShirts() {
        return shirts;
    }

    public void setShirts(String shirts) {
        this.shirts = shirts;
    }

    public String getBurger() {
        return burger;
    }

    public void setBurger(String burger) {
        this.burger = burger;
    }

    public String getSpiderpig() {
        return spiderpig;
    }

    public void setSpiderpig(String spiderpig) {
        this.spiderpig = spiderpig;
    }
}
